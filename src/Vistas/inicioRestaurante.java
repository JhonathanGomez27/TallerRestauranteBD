    private void btnAgregarProdMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnAgregarProdMouseClicked
        String id = jtIdProducto.getText();
        if(id.isEmpty()){
            JOptionPane.showMessageDialog(null, "Ingrese un id para el producto");
            jtIdProducto.setText("");
        }
        String nombre = jtNombreProducto.getText();
        if(nombre.isEmpty()){
            JOptionPane.showMessageDialog(null, "Ingrese un nombre para el producto");
            jtNombreProducto.setText("");
        }
        String descripcion = jtDescripcion.getText();
        if(descripcion.isEmpty()){
            JOptionPane.showMessageDialog(null, "Ingrese una descripci贸n");
            jtDescripcion.setText("");
        }
        Double precio = Double.parseDouble(jtPrecio.getText());
        if(precio <= 0){
            JOptionPane.showMessageDialog(null, "Ingrese un precio para el producto");
            jtPrecio.setText("");
        }
        Integer tiempo = Integer.parseInt(jtTiempoPrep.getText());
        if(tiempo <= 0){
            JOptionPane.showMessageDialog(null, "Ingrese un tiempo de preparaci贸n para el producto");
            jtTiempoPrep.setText("");
        }
      
        String respuesta = control.registroProducto(id,nombre,descripcion,precio,tiempo);
        if(respuesta == null){
        JOptionPane.showMessageDialog(null, "Producto registrado");
        }else{
        JOptionPane.showMessageDialog(null, respuesta);
        } 
    }//GEN-LAST:event_btnAgregarProdMouseClicked

